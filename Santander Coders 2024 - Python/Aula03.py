#AULA DE OPERADORES ARITMÉTICOS E BOOLEANOS

"""
OPERAÇÕES MATEMÁTICAS: 
- Soma: +
- Subtração: -
- Multiplicação: *
- Divisão: /
- Divisão inteira: //
- Resto da divisão: %
- Potência: **
"""
numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 % 3) 
print(2 ** 3) 

# OPERADORES BOOLEANOS EXEMPLOS
idade1 = 10
idade2 = 15
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

print(idade1 > idade2)         #FALSE
print(idade1 <= idade2)        #TRUE
print("Python" == "python")    #FALSE
print("banana" != "abacaxi")   #TRUE
print(altura1 >= altura2)      #TRUE
print(altura2 > altura3)       #FALSE